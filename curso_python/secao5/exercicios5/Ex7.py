while True:
    num1 = int(input('Informe o 1° número:'))
    num2 = int(input('Informe o 2° número:'))
    if num1 > num2:
        print(f'O número {num1} é o maior!')
    elif num1 < num2:
        print(f'O número {num2} é o maior!')
    else:
        print(f'O números {num1} e {num2} são iguais!')


